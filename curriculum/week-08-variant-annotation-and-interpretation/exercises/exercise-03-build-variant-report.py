# ----------------------------------------------------------------------
# CSQ parsing.
# ----------------------------------------------------------------------

def extract_csq_header(vcf_path: Path) -> list[str]:
    """Read the CSQ field order from a VEP-annotated VCF header.

    Args:
        vcf_path: path to the VEP-annotated VCF.

    Returns:
        Ordered list of CSQ field names. Empty list if the VCF has
        no CSQ INFO header.

    Raises:
        RuntimeError if cyvcf2 is not available.
    """
    if not HAS_CYVCF2 or _VCFReader is None:
        raise RuntimeError("cyvcf2 is required for VCF parsing; install it with conda or pip.")
    reader = _VCFReader(str(vcf_path))
    for line in reader.raw_header.splitlines():
        if not line.startswith("##INFO=<ID=CSQ"):
            continue
        marker: str = "Format: "
        idx: int = line.find(marker)
        if idx == -1:
            continue
        format_part: str = line[idx + len(marker):].rstrip('">')
        return format_part.split("|")
    return []


def parse_csq_field(csq_field: str, csq_header: list[str]) -> list[dict[str, str]]:
    """Split a VEP CSQ INFO string into per-transcript dicts.

    Args:
        csq_field:  the raw CSQ value (e.g.
            "A|missense_variant|MODERATE|...|,A|intron_variant|MODIFIER|...|").
            Multiple records are separated by commas; fields within a
            record by pipes.
        csq_header: ordered list of field names from the VCF header.

    Returns:
        list of dicts, one per overlapping transcript record.
    """
    if not csq_field:
        return []
    records: list[dict[str, str]] = []
    for raw in csq_field.split(","):
        fields: list[str] = raw.split("|")
        if len(fields) != len(csq_header):
            # Drop malformed records silently; do not crash.
            continue
        records.append(dict(zip(csq_header, fields)))
    return records


def pick_canonical_record(records: list[dict[str, str]]) -> dict[str, str]:
    """Choose the canonical CSQ record per variant.

    Preference order:
      1. The first record with CANONICAL == "YES".
      2. The first record with a non-empty MANE_SELECT.
      3. The first record overall.
      4. {} if there are no records.

    Args:
        records: list of CSQ-record dicts from parse_csq_field.

    Returns:
        The chosen record (or {}).
    """
    if not records:
        return {}
    canonical = next((r for r in records if r.get("CANONICAL") == "YES"), None)
    if canonical is not None:
        return canonical
    mane = next((r for r in records if r.get("MANE_SELECT")), None)
    if mane is not None:
        return mane
    return records[0]


# ----------------------------------------------------------------------
# SIFT / PolyPhen score parsing.
# ----------------------------------------------------------------------

_SCORE_PATTERN = re.compile(r"([a-zA-Z_]+)\(([-+]?\d*\.?\d+(?:[eE][-+]?\d+)?)\)")


def parse_prediction_and_score(field: str) -> tuple[str, float | None]:
    """Parse a VEP "prediction(score)" string.

    VEP's --sift b and --polyphen b emit strings like:
        deleterious(0.02)
        tolerated(0.5)
        probably_damaging(0.94)
        benign(0.05)
        unknown
        (empty string for variants without a score)

    Args:
        field: the raw VEP SIFT or PolyPhen field value.

    Returns:
        (prediction, score). prediction is "" if the field is empty.
        score is None if no numeric score is present.
    """
    if not field:
        return "", None
    match = _SCORE_PATTERN.match(field)
    if match is None:
        return field, None
    return match.group(1), float(match.group(2))


