def parse_annotated_vcf(vcf_path: Path) -> pd.DataFrame:
    """Read a VEP-annotated VCF and return a per-variant frame.

    Args:
        vcf_path: path to the VEP-annotated VCF.

    Returns:
        pandas DataFrame with one row per variant, columns matching
        DEMO_ROWS.

    Raises:
        RuntimeError if cyvcf2 is not available.
    """
    if not HAS_CYVCF2 or _VCFReader is None:
        raise RuntimeError("cyvcf2 is required for VCF parsing; install it with conda or pip.")

    csq_header: list[str] = extract_csq_header(vcf_path)
    if not csq_header:
        raise RuntimeError(f"{vcf_path} has no CSQ INFO header; was it annotated by VEP?")

    records: list[dict[str, Any]] = []
    reader = _VCFReader(str(vcf_path))
    for variant in reader:
        csq_raw: str = variant.INFO.get("CSQ", "") or ""
        all_records: list[dict[str, str]] = parse_csq_field(csq_raw, csq_header)
        canonical: dict[str, str] = pick_canonical_record(all_records)

        sift_pred, sift_score = parse_prediction_and_score(canonical.get("SIFT", ""))
        pp_pred, pp_score = parse_prediction_and_score(canonical.get("PolyPhen", ""))

        gnomade_af_raw: str = canonical.get("gnomADe_AF", "") or ""
        gnomadg_af_raw: str = canonical.get("gnomADg_AF", "") or ""
        gnomade_af: float | None = _parse_float_or_none(gnomade_af_raw)
        gnomadg_af: float | None = _parse_float_or_none(gnomadg_af_raw)
        # If both are present, take the exome AF as the primary
        # (matches the ACMG convention; the genome AF is recorded
        # separately for completeness).
        primary_af: float | None = gnomade_af if gnomade_af is not None else gnomadg_af

        records.append({
            "chrom": variant.CHROM,
            "pos": int(variant.POS),
            "ref": variant.REF,
            "alt": ",".join(variant.ALT) if variant.ALT else "",
            "gene": canonical.get("SYMBOL", ""),
            "consequence": canonical.get("Consequence", ""),
            "impact": canonical.get("IMPACT", ""),
            "hgvsc": canonical.get("HGVSc", ""),
            "hgvsp": canonical.get("HGVSp", ""),
            "sift_pred": sift_pred,
            "sift_score": sift_score,
            "polyphen_pred": pp_pred,
            "polyphen_score": pp_score,
            "rsid": canonical.get("Existing_variation", ""),
            "gnomad_af": primary_af,
            "gnomad_popmax_af": None,
            "clinvar_clnsig": "",
            "clinvar_clnrevstat": "",
            "clinvar_clndn": "",
        })
    return pd.DataFrame(records)


def _parse_float_or_none(raw: str) -> float | None:
    if not raw:
        return None
    try:
        return float(raw)
    except ValueError:
        return None


