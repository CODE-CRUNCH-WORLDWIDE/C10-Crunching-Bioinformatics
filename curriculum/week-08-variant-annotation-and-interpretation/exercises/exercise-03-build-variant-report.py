def join_with_exercise_02(report: pd.DataFrame, exercise_02_tsv: Path) -> pd.DataFrame:
    """Merge the report frame with the Exercise 2 TSV on coordinates.

    The Exercise 2 TSV has chrom (no "chr" prefix); the VEP VCF has
    "chr" prefixes. Normalize before joining.
    """
    if not exercise_02_tsv.exists():
        print(f"  [warn] {exercise_02_tsv} not found; skipping ClinVar/gnomAD join.")
        return report
    ex02: pd.DataFrame = pd.read_csv(exercise_02_tsv, sep="\t")
    ex02 = ex02.copy()
    ex02["chrom_norm"] = ex02["chrom"].astype(str).str.replace("^chr", "", regex=True)
    report = report.copy()
    report["chrom_norm"] = report["chrom"].astype(str).str.replace("^chr", "", regex=True)
    merged: pd.DataFrame = report.merge(
        ex02[["chrom_norm", "pos", "ref", "alt", "gnomad_af", "gnomad_popmax_af",
              "clinvar_clnsig", "clinvar_clnrevstat", "clinvar_clndn"]],
        on=["chrom_norm", "pos", "ref", "alt"],
        how="left",
        suffixes=("", "_ex02"),
    )
    # Prefer Exercise-2 values over the VEP-bundled gnomAD AF when present.
    for col in ("gnomad_af", "gnomad_popmax_af", "clinvar_clnsig",
                "clinvar_clnrevstat", "clinvar_clndn"):
        ex02_col: str = f"{col}_ex02"
        if ex02_col in merged.columns:
            merged[col] = merged[ex02_col].combine_first(merged[col])
            merged = merged.drop(columns=[ex02_col])
    merged = merged.drop(columns=["chrom_norm"])
    return merged


