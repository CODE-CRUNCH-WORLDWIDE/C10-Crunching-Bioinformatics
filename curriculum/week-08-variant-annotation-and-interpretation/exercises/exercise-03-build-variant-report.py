def render_html_report(report: pd.DataFrame, output_path: Path,
                        run_info: dict[str, Any]) -> None:
    """Render the per-variant frame as a standalone HTML report."""
    table_html: str = report.to_html(index=False, na_rep="", escape=True,
                                       classes="report-table")

    # Manual templating (no Jinja2 required for this simple form),
    # but the placeholders use Jinja-like syntax for readability.
    html: str = HTML_TEMPLATE
    replacements: dict[str, str] = {
        "{{ disclaimer }}": DISCLAIMER.split(". ", 1)[1].rstrip("."),
        "{{ run_date }}": str(run_info.get("run_date", "")),
        "{{ input_vcf }}": str(run_info.get("input_vcf", "")),
        "{{ vep_cache_version }}": str(run_info.get("vep_cache_version", "")),
        "{{ clinvar_release }}": str(run_info.get("clinvar_release", "")),
        "{{ gnomad_version }}": str(run_info.get("gnomad_version", "")),
        "{{ n_variants }}": str(run_info.get("n_variants", len(report))),
        "{{ table_html | safe }}": table_html,
    }
    for key, value in replacements.items():
        html = html.replace(key, value)
    output_path.write_text(html, encoding="utf-8")


