def parse_args(argv: list[str] | None = None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Build a per-variant interpretation report from a VEP-"
                    "annotated VCF. Educational and research use only. Not "
                    "for clinical decision-making."
    )
    parser.add_argument("--vcf", type=Path,
                        default=Path(__file__).parent / "data" / "demo.vep.vcf",
                        help="Input VEP-annotated VCF.")
    parser.add_argument("--ex02-tsv", type=Path,
                        default=Path(__file__).parent / "results" / "exercise-02-clinvar-gnomad.tsv",
                        help="Exercise 2 output TSV (ClinVar + gnomAD).")
    parser.add_argument("--out-csv", type=Path,
                        default=Path(__file__).parent / "results" / "exercise-03-variants.csv",
                        help="Output CSV.")
    parser.add_argument("--out-html", type=Path,
                        default=Path(__file__).parent / "results" / "exercise-03-variants.html",
                        help="Output HTML.")
    parser.add_argument("--out-run-info", type=Path,
                        default=Path(__file__).parent / "results" / "exercise-03-run-info.json",
                        help="Output run-info JSON.")
    parser.add_argument("--clinvar-release", type=str, default="2024-09-01",
                        help="ClinVar VCF release date (recorded in run-info JSON).")
    parser.add_argument("--gnomad-version", type=str, default="v4.1.0",
                        help="gnomAD version (recorded in run-info JSON).")
    parser.add_argument("--vep-cache-version", type=str, default="110",
                        help="VEP cache version (recorded in run-info JSON).")
    parser.add_argument("--no-network", action="store_true",
                        help="Skip VCF parsing and use the bundled demo rows.")
    return parser.parse_args(argv)


def main(argv: list[str] | None = None) -> int:
    args = parse_args(argv)
    args.out_csv.parent.mkdir(parents=True, exist_ok=True)

    if args.no_network or not args.vcf.exists() or not HAS_CYVCF2:
        if not args.no_network:
            print(f"  [info] Falling back to bundled demo rows: vcf exists={args.vcf.exists()}, "
                  f"cyvcf2 available={HAS_CYVCF2}.")
        report = demo_rows_to_dataframe(DEMO_ROWS)
        input_label: str = "demo (in-memory)"
    else:
        print(f"  [info] Parsing {args.vcf} with cyvcf2 ...")
        report = parse_annotated_vcf(args.vcf)
        input_label = str(args.vcf)
        if args.ex02_tsv.exists():
            print(f"  [info] Joining with {args.ex02_tsv} ...")
            report = join_with_exercise_02(report, args.ex02_tsv)

    # Write CSV.
    report.to_csv(args.out_csv, index=False)
    print(f"  [info] wrote {args.out_csv} with {len(report)} rows.")

    # Write run-info JSON.
    run_info: dict[str, Any] = {
        "run_date": dt.datetime.now().date().isoformat(),
        "input_vcf": input_label,
        "vep_cache_version": args.vep_cache_version,
        "clinvar_release": args.clinvar_release,
        "gnomad_version": args.gnomad_version,
        "n_variants": int(len(report)),
        "disclaimer": DISCLAIMER,
    }
    args.out_run_info.write_text(json.dumps(run_info, indent=2, sort_keys=True), encoding="utf-8")
    print(f"  [info] wrote {args.out_run_info}.")

    # Write HTML.
    render_html_report(report, args.out_html, run_info)
    print(f"  [info] wrote {args.out_html}.")

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
